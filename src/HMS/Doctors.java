    boolean checkIDC() {
    boolean idMatch = false;
    boolean contactMatch = false;
    
    for (Doctor p : allDoctors) {
        if (id.getText().equals(String.valueOf(p.getId()))) {
            idMatch = true;
        }
        
        if (contact.getText().equals(p.getContact())) {
            contactMatch = true;
        }
        
        // If both ID and contact match, return true
        if (idMatch && contactMatch) {
            return true;
        }
    }
    
    // If either ID or contact match, return true
    if (idMatch || contactMatch) {
        return true;
    }
    
    return false;
}

    
    void saveAllData() {
    try {
        FileWriter fileWriter = new FileWriter("ddata.txt");
        for (Doctor doctor : allDoctors) {
            fileWriter.write(doctor.getId() + ";" + doctor.getName() + ";" + doctor.getSpecification() + ";" + doctor.getGender() + ";" + doctor.getFees() + ";" + doctor.getContact() + "\n");
        }
        fileWriter.close();
    } catch (IOException e) {
        e.printStackTrace();
    }
}
    
    
