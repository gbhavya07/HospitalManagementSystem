     public void writeAllData() {
        allDData.setText("");
        for (Doctor doctor : allDoctors) {
            allDData.append(doctor.getId() + "   " + doctor.getName() + "   " + doctor.getSpecification() + "   " + doctor.getGender() + "   " + doctor.getFees() + "   " + doctor.getContact() + "\n"); // Added newline character
        }
    }

    public void readAllData() {
        try {
            File pfile = new File("ddata.txt");
            Scanner scanner = new Scanner(pfile);
            while (scanner.hasNextLine()) {
                String data = scanner.nextLine();
                String[] curData = data.split(";");
                Doctor doctor = new Doctor();
                doctor.setId(Integer.parseInt(curData[0]));

                doctor.setName(curData[1]);
                doctor.setSpecification(curData[2]);
                doctor.setGender(curData[3]);
                doctor.setFees(Integer.parseInt(curData[4]));
                doctor.setContact(curData[5]);
                allDoctors.add(doctor);
            }
            scanner.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
